#en si los datos solos no hacen nada, pero podemos usar operadores para manipularlos y obtener resultados, asi que vamos a ver algunos operadores basicos:

#operadores aritmeticos: +, -, *, /, //, %, **
#operadores de comparacion: ==, !=, >, <, >=, <=
#operadores logicos: and, or, not
#operadores de asignacion: =, +=, -=, *=, /=, //=, %=, **=

#para poder entendir cada uno, es mejor ver un ejemplo

suma=2+3
resta=5-2
multiplicacion=4*3
division=10/2
division_entera=10//3
modulo=10%3
potencia=2**3

#los operadores aritmeticos se utilizan para realizar operaciones matematicas, como sumar, restar, multiplicar, dividir, etc

comparacion1=5==5
comparacion2=5!=3
comparacion3=5>3
comparacion4=5<3
comparacion5=5>=5
comparacion6=5<=3

#los operadores de comparacion se utilizan para comparar dos valores y obtener un resultado booleano, es decir, True o False

logico1=True and False
logico2=True or False
logico3=not True

#los operadores logicos se utilizan para combinar valores booleanos y obtener un resultado booleano, es decir, True o False

asignacion1=10
asignacion1+=5
asignacion1-=3
asignacion1*=2
asignacion1/=2
asignacion1//=2
asignacion1%=2
asignacion1**=2

#los operadores de asignacion se utilizan para asignar un valor a una variable, y pueden combinarse con operadores aritmeticos para realizar operaciones y asignar el resultado a la variable